namespace Cscfa\Bundle\ToolboxBundle\Strings;

/**
 * StringTool class.
 *
 * The StringTool class is used
 * to affect string.
 *
 * @category Object
 *
 * @author   Matthieu VALLANCE <matthieu.vallance@cscfa.fr>
 * @license  http://opensource.org/licenses/MIT MIT
 *
 * @link     http://cscfa.fr
 */
class StringTool
{
    public static function limitLength($string, $lenght, $limiter = '')
    {
        return strlen($string) > $lenght ? substr($string, 0, $lenght).$limiter : $string;
    }
}
